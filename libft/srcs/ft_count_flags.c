#include "libftprintf.h"

int		ft_count_flags(char *s)
{
	int i;

	i = 0;
	while (s[i] == '-' || s[i] == '+' || s[i] == ' ' ||
			s[i] == '#' || s[i] == '0')
		i++;
	return (i);
}

int		ft_count_digits(char *s)
{
	int i;

	i = 0;
	while (s[i] >= '0' && s[i] <= '9')
		i++;
	return (i);
}
