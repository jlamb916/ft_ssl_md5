#include "ft_ssl.h"

void		append_zero(char *s)
{
	int i;

	i = ft_strlen(s);
	while (i < 8)
	{
		ft_printf("0");
		i++;
	}
}

uint32_t	reverse_uint32(uint32_t n)
{
	return ((n >> 24) | ((n & 0xff0000) >> 8) |
			((n & 0xff00) << 8) | (n << 24));
}
